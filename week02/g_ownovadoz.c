#include <stdio.h>

int pow[33] = { 1,2,4,8,16,32,64,128,256,512,1024,2048,4096,8192,16384,32768,65536,131072,262144,524288,1048576,2097152,4194304,8388608,16777216,33554432,67108864,134217728, 268435456, 536870912, 1073741824, 2147483648, 4294967296 };
int sum[33] = { 1,2,5,13,33,81,193,449,1025,2305,5121,11265,24577,53249,114689,245761,524289,1114113,2359297,4980737,10485761,22020097,46137345,96468993,201326593,419430401,872415233,1811939329,3758096385,7784628225,16106127361,33285996545,68719476737 };

long long int ans;

void solution(int n) {
	if (n == 1) {
		ans += 1;
		return;
	}
	for (int i = 0; i < 33; i++) {
		if (n == pow[i]) {
			ans += sum[i];
			return;
		}
		if (n > pow[i]) continue;
		ans += sum[i - 1] + n - pow[i - 1];
		solution(n - pow[i - 1]);
		break;
	}
}

int main(void) {

	int n;

	scanf("%d", &n);

	solution(n);

	printf("%lld\n", ans);

	return 0;
}